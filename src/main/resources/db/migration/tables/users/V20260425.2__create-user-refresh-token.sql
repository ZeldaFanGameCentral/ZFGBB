create table zfgbb.user_refresh_token (
	user_refresh_token_id integer not null primary key generated by default as identity (sequence name seq_user_refresh_token_id),
	user_id integer not null references zfgbb.user,
	token_hash varchar(64) not null unique,
	issued_ts timestamp without time zone default current_timestamp not null,
	expires_ts timestamp without time zone not null,
	revoked_flag boolean default false not null,
	created_ts timestamp without time zone default current_timestamp not null,
	updated_ts timestamp without time zone default current_timestamp not null
);

create index idx_user_refresh_token_user_id
on zfgbb.user_refresh_token (user_id);
